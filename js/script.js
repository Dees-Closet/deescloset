/* ==================== START LOAD CART ==================== */
function loadCart() {
    try {
        const savedCart = localStorage.getItem('deesClosetCart');
        if (savedCart) {
            cart = JSON.parse(savedCart);
            console.log('Cart loaded from localStorage:', cart.length, 'items');
        } else {
            cart = [];
            console.log('No cart found in localStorage');
        }
    } catch (e) {
        console.error('Error loading cart:', e);
        cart = [];
    }
}
/* ==================== END LOAD CART ==================== */

/* ==================== START EVENT LISTENERS ==================== */
function setupEventListeners() {
    console.log('Setting up event listeners...');
    
    // Cart toggle button
    const cartToggle = document.getElementById('cart-toggle');
    if (cartToggle) {
        console.log('Cart toggle button found');
        cartToggle.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            console.log('Cart toggle clicked!');
            toggleCart();
        });
    } else {
        console.error('Cart toggle button NOT found!');
    }
    
    // Close cart button
    const closeCart = document.getElementById('close-cart');
    if (closeCart) {
        console.log('Close cart button found');
        closeCart.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            toggleCart();
        });
    } else {
        console.error('Close cart button NOT found!');
    }
    
    // Cart overlay
    const cartOverlay = document.getElementById('cart-overlay');
    if (cartOverlay) {
        console.log('Cart overlay found');
        cartOverlay.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            toggleCart();
        });
    } else {
        console.error('Cart overlay NOT found!');
    }
    
    // Checkout WhatsApp button
    const checkoutWhatsApp = document.getElementById('checkout-whatsapp');
    if (checkoutWhatsApp) {
        checkoutWhatsApp.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            checkoutViaWhatsApp();
        });
    }
    
    // Category filter dropdown
    const categoryFilter = document.getElementById('category-filter');
    if (categoryFilter) {
        categoryFilter.addEventListener('change', function() {
            const category = this.value;
            const sort = document.getElementById('price-filter').value;
            filterAndSortProducts(category, sort);
        });
    }
    
    // Price filter dropdown
    const priceFilter = document.getElementById('price-filter');
    if (priceFilter) {
        priceFilter.addEventListener('change', function() {
            const sort = this.value;
            const category = document.getElementById('category-filter').value;
            filterAndSortProducts(category, sort);
        });
    }
    
    // Mobile menu toggle
    const mobileMenuToggle = document.getElementById('mobile-menu-toggle');
    const navMenu = document.querySelector('.nav-menu');
    if (mobileMenuToggle && navMenu) {
        mobileMenuToggle.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            navMenu.classList.toggle('active');
            console.log('Mobile menu toggled:', navMenu.classList.contains('active'));
        });
        
        // Close mobile menu on link click
        const navLinks = navMenu.querySelectorAll('a');
        navLinks.forEach(link => {
            link.addEventListener('click', () => {
                navMenu.classList.remove('active');
            });
        });
    }
    
    // Click outside to close cart and mobile menu
    document.addEventListener('click', function(e) {
        const cartModal = document.getElementById('cart-modal');
        const cartToggle = document.getElementById('cart-toggle');
        
        // Close cart when clicking outside
        if (cartModal && cartModal.classList.contains('active')) {
            if (!cartModal.contains(e.target) && !cartToggle.contains(e.target)) {
                toggleCart();
            }
        }
        
        // Close mobile menu when clicking outside
        if (navMenu && navMenu.classList.contains('active')) {
            if (!navMenu.contains(e.target) && !mobileMenuToggle.contains(e.target)) {
                navMenu.classList.remove('active');
            }
        }
    });
    
    // Close on ESC key
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape') {
            const cartModal = document.getElementById('cart-modal');
            
            if (cartModal && cartModal.classList.contains('active')) {
                toggleCart();
            }
            if (navMenu && navMenu.classList.contains('active')) {
                navMenu.classList.remove('active');
            }
        }
    });
    
    console.log('Event listeners setup complete!');
}
/* ==================== END EVENT LISTENERS ==================== */

/* ==================== START FILTER AND SORT PRODUCTS ==================== */
function filterAndSortProducts(category, sort) {
    currentIndex = 0;
    currentSort = sort;
    
    // Remove existing load more button
    const existingBtn = document.querySelector('.load-more-container');
    if (existingBtn) {
        existingBtn.remove();
    }
    
    // Filter by category
    if (category === 'all') {
        displayedProducts = [...productsData];
    } else {
        displayedProducts = productsData.filter(product => product.category === category);
    }
    
    // Sort by price
    if (sort === 'low-to-high') {
        displayedProducts.sort((a, b) => a.price - b.price);
    } else if (sort === 'high-to-low') {
        displayedProducts.sort((a, b) => b.price - a.price);
    }
    
    // Render products
    renderProducts(displayedProducts.slice(0, Math.min(INITIAL_DISPLAY, displayedProducts.length)));
    loadMoreButton();
    
    console.log('Filtered:', category, 'Sorted:', sort, 'Products:', displayedProducts.length);
}
/* ==================== END FILTER AND SORT PRODUCTS ==================== */

/* ==================== START TOGGLE CART ==================== */
function toggleCart() {
    const cartModal = document.getElementById('cart-modal');
    const cartOverlay = document.getElementById('cart-overlay');
    
    if (!cartModal || !cartOverlay) {
        console.error('Cart modal or overlay not found!');
        return;
    }
    
    const isActive = cartModal.classList.toggle('active');
    cartOverlay.classList.toggle('active');
    
    console.log('Cart toggled. Active:', isActive);
    
    if (isActive) {
        renderCart();
    }
}
/* ==================== END TOGGLE CART ==================== */

/* ==================== START RENDER PRODUCTS ==================== */
function renderProducts(products) {
    const productGrid = document.getElementById('product-grid');
    
    if (!productGrid) {
        console.error('Product grid not found!');
        return;
    }
    
    productGrid.innerHTML = '';
    
    if (products.length === 0) {
        productGrid.innerHTML = '<p style="text-align: center; grid-column: 1/-1; padding: 40px; color: var(--color-text);">No products found.</p>';
        return;
    }
    
    console.log('Rendering', products.length, 'products');
    
    products.forEach(product => {
        const productCard = document.createElement('div');
        productCard.classList.add('product-card');
        productCard.dataset.id = product.id;
        
        const sliderDots = product.images.map((_, index) => 
            `<span class="slider-dot ${index === 0 ? 'active' : ''}" data-index="${index}"></span>`
        ).join('');
        
        const imagesHTML = product.images.map((img, index) => 
            `<img src="${img}" alt="${product.name}" class="${index === 0 ? 'active' : ''}" onerror="this.src='https://picsum.photos/400/500?random=${product.id + index}'">`
        ).join('');
        
        productCard.innerHTML = `
            <div class="product-slider" data-product-id="${product.id}">
                ${imagesHTML}
                ${product.images.length > 1 ? `
                    <button class="slider-btn prev" type="button" data-direction="prev">
                        <i class="fa-solid fa-chevron-left"></i>
                    </button>
                    <button class="slider-btn next" type="button" data-direction="next">
                        <i class="fa-solid fa-chevron-right"></i>
                    </button>
                ` : ''}
                <div class="slider-dots">
                    ${sliderDots}
                </div>
            </div>
            <div class="product-info">
                <span class="product-category">${product.category}</span>
                <h3 class="product-name">${product.name}</h3>
                <p class="product-price">Kes ${product.price.toLocaleString()}</p>
                <div class="product-actions">
                    <button class="btn btn-outline btn-sm add-to-cart" type="button" data-id="${product.id}">
                        <i class="fa-solid fa-cart-plus"></i> Add to Cart
                    </button>
                    <button class="btn btn-primary btn-sm buy-now" type="button" data-id="${product.id}">
                        <i class="fa-brands fa-whatsapp"></i> Buy Now
                    </button>
                </div>
            </div>
        `;
        
        productGrid.appendChild(productCard);
        setupProductSlider(productCard);
    });
    
    setupProductButtons();
}
/* ==================== END RENDER PRODUCTS ==================== */

