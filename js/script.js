/* ==================== START RENDER CART ==================== */
function renderCart() {
    const cartItems = document.getElementById('cart-items');
    const cartTotalPrice = document.getElementById('cart-total-price');
    
    if (!cartItems) {
        console.error('Cart items container not found!');
        return;
    }
    
    console.log('Rendering cart with', cart.length, 'items');
    
    if (cart.length === 0) {
        cartItems.innerHTML = '<p class="empty-cart-message">Your cart is empty.</p>';
        if (cartTotalPrice) {
            cartTotalPrice.textContent = 'Kes 0';
        }
        return;
    }
    
    cartItems.innerHTML = cart.map(item => `
        <div class="cart-item" data-id="${item.id}">
            <img src="${item.images[0]}" alt="${item.name}" class="cart-item-image" onerror="this.src='https://picsum.photos/70/70?random=${item.id}'">
            <div class="cart-item-details">
                <h4 class="cart-item-name">${item.name}</h4>
                <p class="cart-item-price">Kes ${item.price.toLocaleString()} x ${item.quantity}</p>
                <button class="cart-item-remove" type="button" data-id="${item.id}">
                    <i class="fa-solid fa-trash"></i> Remove
                </button>
            </div>
        </div>
    `).join('');
    
    const total = cart.reduce((sum, item) => sum + (item.price * item.quantity), 0);
    if (cartTotalPrice) {
        cartTotalPrice.textContent = `Kes ${total.toLocaleString()}`;
    }
    
    document.querySelectorAll('.cart-item-remove').forEach(btn => {
        btn.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            const productId = parseInt(btn.dataset.id);
            removeFromCart(productId);
        });
    });
}
/* ==================== END RENDER CART ==================== */

/* ==================== START REMOVE FROM CART ==================== */
function removeFromCart(productId) {
    cart = cart.filter(item => item.id !== productId);
    saveCart();
    updateCartCount();
    renderCart();
    showNotification('Item removed from cart!');
}
/* ==================== END REMOVE FROM CART ==================== */

/* ==================== START UPDATE CART COUNT ==================== */
function updateCartCount() {
    const cartCount = document.getElementById('cart-count');
    
    if (!cartCount) {
        console.error('Cart count element not found!');
        return;
    }
    
    const totalItems = cart.reduce((sum, item) => sum + item.quantity, 0);
    cartCount.textContent = totalItems;
    
    if (totalItems > 0) {
        cartCount.style.display = 'flex';
    } else {
        cartCount.style.display = 'none';
    }
    
    console.log('Cart count updated:', totalItems);
}
/* ==================== END UPDATE CART COUNT ==================== */

/* ==================== START SAVE CART ==================== */
function saveCart() {
    try {
        localStorage.setItem('deesClosetCart', JSON.stringify(cart));
        console.log('Cart saved to localStorage:', cart);
    } catch (e) {
        console.error('Error saving cart to localStorage:', e);
    }
}
