/* ==================== START PRODUCT SLIDER ==================== */
function setupProductSlider(productCard) {
    const slider = productCard.querySelector('.product-slider');
    if (!slider) return;
    
    const images = slider.querySelectorAll('img');
    const dots = slider.querySelectorAll('.slider-dot');
    const prevBtn = slider.querySelector('.slider-btn.prev');
    const nextBtn = slider.querySelector('.slider-btn.next');
    
    let currentSlideIndex = 0;
    
    function updateSlider(index) {
        images.forEach((img, i) => {
            img.classList.toggle('active', i === index);
        });
        dots.forEach((dot, i) => {
            dot.classList.toggle('active', i === index);
        });
        currentSlideIndex = index;
    }
    
    if (prevBtn) {
        prevBtn.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            const newIndex = currentSlideIndex === 0 ? images.length - 1 : currentSlideIndex - 1;
            updateSlider(newIndex);
        });
    }
    
    if (nextBtn) {
        nextBtn.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            const newIndex = currentSlideIndex === images.length - 1 ? 0 : currentSlideIndex + 1;
            updateSlider(newIndex);
        });
    }
    
    dots.forEach((dot, index) => {
        dot.addEventListener('click', function(e) {
            e.preventDefault();
            e.stopPropagation();
            updateSlider(index);
        });
    });
}
/* ==================== END PRODUCT SLIDER ==================== */

